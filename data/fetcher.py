class DataFrame:
    """Simple DataFrame-like class to mimic pandas behavior."""

    def __init__(self, data, columns):
        self.columns = columns
        self.data = []

        for row in data:
            row_dict = {}
            for i, col in enumerate(columns):
                row_dict[col] = row[i]
            self.data.append(row_dict)

    def __len__(self):
        return len(self.data)

    def __getitem__(self, key):
        if isinstance(key, str):
            # Single column
            return [row[key] for row in self.data]
        elif isinstance(key, list):
            # Multiple columns
            result = DataFrame([], key)
            for row in self.data:
                new_row = tuple(row[col] for col in key)
                result.data.append(dict(zip(key, new_row)))
            return result

    def to_csv(self, filepath):
        """Write to CSV file."""
        with open(filepath, 'w', newline='', encoding='utf-8') as f:
            writer = csv.DictWriter(f, fieldnames=self.columns, extrasaction='ignore')
            writer.writeheader()
            writer.writerows(self.data)

    def head(self, n=5):
        """Return first n rows."""
        result = DataFrame([], self.columns)
        result.data = self.data[:n]
        return result

    def __str__(self):
        """Print formatted table."""
        if not self.data:
            return "Empty DataFrame"

        # Print header
        header = " | ".join(str(col)[:15] for col in self.columns)
        print(header)
        print("-" * len(header))

        # Print rows
        for row in self.data:
            values = [str(row.get(col, ''))[:15] for col in self.columns]
            print(" | ".join(values))

        return ""


